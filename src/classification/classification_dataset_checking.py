def count_classes(path, name):

    print(f"\n========== {name} CLASS COUNT ==========\n")

    counts = {}
    total = 0

    for cls in sorted(os.listdir(path)):

        folder = os.path.join(path,cls)

        if os.path.isdir(folder):

            n = len(os.listdir(folder))
            counts[cls] = n
            total += n

            print(f"{cls:15s} : {n}")

    print("Total:", total)

    return counts


