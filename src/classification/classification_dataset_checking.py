def check_corrupt(path, name):

    print(f"\n========== {name} CORRUPT CHECK ==========\n")

    bad = 0

    for cls in os.listdir(path):

        folder = os.path.join(path,cls)

        for f in os.listdir(folder):

            p = os.path.join(folder,f)

            try:
                img = Image.open(p)
                img.verify()
            except:
                bad += 1
                print("Corrupt:", p)

    print("Total corrupt:", bad)

    return bad


