def collect_hashes(path):

    hashes = {}

    for cls in os.listdir(path):

        folder = os.path.join(path,cls)

        for f in os.listdir(folder):

            p = os.path.join(folder,f)

            with open(p,"rb") as file:
                h = hashlib.md5(file.read()).hexdigest()

            hashes[h] = p

    return hashes


